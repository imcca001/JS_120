// Textual Discription of Tic Tac Toe
 // - Tic Tac Toe is a 2-player board game
 // - The board is a 3x3 grid
 // - Players take turns marking a square that identifies the Player
 // - Traditionally, the player to go first uses the marker X to mark 
 //   her squares, and the player to go second uses the marker 0.
 // - The first player to mark 3 squares in a row with her marker 
 //   wins the game.
 // - A Row can be a horizontal row, a vertical column, or either of 
 //   the two diagonals (top-left to bottom-right and top-right
 //    to bottom-left).
 // - There is one human player and one computer player.
 // - The human player always moves (places a marker) first in 
 //   the initial version of our game; you can change that later.

